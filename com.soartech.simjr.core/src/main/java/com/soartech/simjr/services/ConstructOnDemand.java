package com.soartech.simjr.services;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * An annotation that can be added to service classes to indicate that they
 * can be automatically constructed by the service manager when requested.
 * 
 * @author ray
 * @see SimulationService
 * @see ServiceManager
 */
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.TYPE)
public @interface ConstructOnDemand
{
}
